public class HorizontalError {

    private int z;
    private int y;

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "HorizontalError{" +
                "z=" + z +
                ", y=" + y +
                '}';
    }
}
