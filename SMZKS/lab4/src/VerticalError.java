public class VerticalError {

    private int z;
    private int x;

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    @Override
    public String toString() {
        return "VerticalError{" +
                "z=" + z +
                ", x=" + x +
                '}';
    }
}
